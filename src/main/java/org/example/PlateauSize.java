package org.example;

public class PlateauSize {

    private int xAxis;
    private int yAxis;


    public PlateauSize(int xAxis, int yAxis) {
        this.xAxis = xAxis;
        this.yAxis = yAxis;
    }

    public int getxAxis() {
        return xAxis;
    }

    public int getyAxis() {
        return yAxis;
    }
}
